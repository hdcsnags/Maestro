Deno.serve(async (req: Request) => {
  const corsHeaders = buildCorsHeaders(req);
  if (req.method === "OPTIONS") {
    return new Response(null, { status: 200, headers: corsHeaders });
  }

  try {
    const auth = await requireAuthenticatedRequest(req, corsHeaders, "github-execute");
    if (auth instanceof Response) {
      return auth;
    }

    const { adminClient: supabase, authHeader, userId } = auth;
    const supabaseUrl = Deno.env.get("SUPABASE_URL")!;

    const bodyResult = await readJsonBody<ExecuteRequest>(req, corsHeaders, {
      maxBytes: GITHUB_EXECUTE_MAX_BODY_BYTES,
      label: "GitHub execute request body",
    });
    if (bodyResult instanceof Response) {
      return bodyResult;
    }
    const body = bodyResult;
    const { mode, repo_connection_id, execution_run_id, session_id, synthesis_content, commit_message } = body;
    if (mode !== "per_agent" && mode !== "synthesized") {
      return new Response(
        JSON.stringify({
          error: "INVALID_EXECUTION_MODE",
          message: "github-execute requires mode to be either per_agent or synthesized.",
        }),
        { status: 400, headers: { ...corsHeaders, "Content-Type": "application/json" } },
      );
    }

    // Hard guard: patches must be a non-empty array. Frontend can send an
    // empty/undefined patches array if the user fires Build before any
    // responses are selected — every downstream .map/.flatMap would crash.
    const patches: AgentPatch[] = Array.isArray(body.patches) ? body.patches : [];
    if (patches.length === 0) {
      return new Response(
        JSON.stringify({
          error: "NO_PATCHES",
          message: "No patches were submitted. Select at least one agent response before executing.",
        }),
        { status: 400, headers: { ...corsHeaders, "Content-Type": "application/json" } },
      );
    }

    let repoConn: Record<string, unknown> | null = null;
    if (repo_connection_id) {
      const { data } = await supabase
        .from("repo_connections")
        .select("*")
        .eq("id", repo_connection_id)
        .eq("user_id", userId)
        .maybeSingle();
      repoConn = data;
    }

    // Fallback — if the explicit id is missing or stale (frontend state drift,
    // cross-workspace switch, deleted row) look up the active repo connection
    // for the session's workspace. This unblocks builds when the cached id
    // no longer points anywhere valid.
    if (!repoConn && session_id) {
      const { data: sessRow } = await supabase
        .from("sessions")
        .select("workspace_id")
        .eq("id", session_id)
        .eq("user_id", userId)
        .maybeSingle();
      const wsId = (sessRow as { workspace_id?: string } | null)?.workspace_id;
      if (wsId) {
        const { data: activeConn } = await supabase
          .from("repo_connections")
          .select("*")
          .eq("workspace_id", wsId)
          .eq("user_id", userId)
          .eq("is_active", true)
          .order("created_at", { ascending: false })
          .limit(1)
          .maybeSingle();
        repoConn = activeConn;
      }
    }

    if (!repoConn) {
      return new Response(
        JSON.stringify({
          error: "Repo connection not found",
          message: `No repo connection matched id "${repo_connection_id ?? "(none)"}" and no active repo connection exists for this session's workspace. Reconnect or pick a repo in the New App panel.`,
          repo_connection_id: repo_connection_id ?? null,
        }),
        { status: 404, headers: { ...corsHeaders, "Content-Type": "application/json" } },
      );
    }

    const ghToken = await getDecryptedSecret(supabase, userId, "github");

    if (!ghToken) {
      return new Response(JSON.stringify({ error: "GitHub not connected" }), {
        status: 400,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }
    const owner = repoConn.owner;
    const repo = repoConn.repo;
    const defaultBranch = repoConn.default_branch || "main";

    // P11 — Elevated runs must reference a valid, scope-matching approval_request.
    const { data: runRow } = await supabase
      .from("execution_runs")
      .select("execution_mode")
      .eq("id", execution_run_id)
      .eq("user_id", userId)
      .maybeSingle();

    // Sprint A · B4 — build spec freeze gate. Any non-analyze run requires the
    // session to have completed pre-build and locked its build_spec. Analyze
    // mode bypasses this. Hard gate, no override.
    if (runRow?.execution_mode && runRow.execution_mode !== "analyze") {
      if (!session_id) {
        return new Response(
          JSON.stringify({
            error: "BUILD_SPEC_NOT_LOCKED",
            message: "Execution run is missing session_id. Cannot verify build spec lock.",
          }),
          { status: 400, headers: { ...corsHeaders, "Content-Type": "application/json" } },
        );
      }
      const { data: sessionRow } = await supabase
        .from("sessions")
        .select("build_spec_locked, current_phase")
        .eq("id", session_id)
        .maybeSingle();

      if (!sessionRow?.build_spec_locked) {
        return new Response(
          JSON.stringify({
            error: "BUILD_SPEC_NOT_LOCKED",
            message: "Complete pre-build phase and lock build spec before executing.",
          }),
          { status: 400, headers: { ...corsHeaders, "Content-Type": "application/json" } },
        );
      }

      // Sprint B · B5 — build lane assignment gate. Mirrors build_spec_locked.
      // pr_flow + elevated both blocked without lanes. Analyze bypasses (above).
      const { data: laneRows } = await supabase
        .from("build_lanes")
        .select("agent_id, agent_name, lane_paths, role")
        .eq("session_id", session_id);

      const writingPatches = patches.filter(hasWriteManifest);
      const builderLanes = (laneRows ?? []).filter((l) => l.role === "builder");
      if (writingPatches.length > 0 && builderLanes.length === 0) {
        return new Response(
          JSON.stringify({
            error: "LANES_NOT_ASSIGNED",
            message: "Code-writing builder agents must have non-overlapping lane assignments before build can execute",
          }),
          { status: 400, headers: { ...corsHeaders, "Content-Type": "application/json" } },
        );
      }

      // Only patches that contain actual file writes require a builder lane.
      // Reviewer/read_only/prose-only responses may be included in the run
      // context, but they must not block execution just because they lack
      // build_lanes rows.
      // Match by agent_id first (reliable), then by agent_name (fallback).
      const laneAgentIds = new Set(builderLanes.map((l) => l.agent_id).filter(Boolean));
      const laneAgentNames = new Set(builderLanes.map((l) => l.agent_name));
      const missing = writingPatches
        .map((p) => p.agent_name)
        .filter((n, i) => {
          const patchAgentId = writingPatches[i].agent_id;
          return !laneAgentIds.has(patchAgentId) && !laneAgentNames.has(n);
        });
      if (missing.length > 0) {
        return new Response(
          JSON.stringify({
            error: "LANES_NOT_ASSIGNED",
            message: `Builder agents missing lane assignment: ${missing.join(", ")}`,
          }),
          { status: 400, headers: { ...corsHeaders, "Content-Type": "application/json" } },
        );
      }

      // No two builder lanes may share a path
      const seen = new Map<string, string>();
      for (const lane of builderLanes) {
        for (const p of (lane.lane_paths as string[] | null) ?? []) {
          if (seen.has(p)) {
            return new Response(
              JSON.stringify({
                error: "LANES_NOT_ASSIGNED",
                message: `Lane path "${p}" is claimed by both ${seen.get(p)} and ${lane.agent_name}`,
              }),
              { status: 400, headers: { ...corsHeaders, "Content-Type": "application/json" } },
            );
          }
          seen.set(p, lane.agent_name);
        }
      }
    }

    if (runRow?.execution_mode === "elevated") {
      if (!body.approval_request_id) {
        return new Response(
          JSON.stringify({ error: "Elevated execution requires approval_request_id" }),
          { status: 403, headers: { ...corsHeaders, "Content-Type": "application/json" } },
        );
      }

      const { data: approval } = await supabase
        .from("approval_requests")
        .select("*")
        .eq("id", body.approval_request_id)
        .eq("user_id", userId)
        .maybeSingle();

      if (!approval || approval.status !== "approved") {
        return new Response(
          JSON.stringify({ error: "Approval request not found or not approved" }),
          { status: 403, headers: { ...corsHeaders, "Content-Type": "application/json" } },
        );
      }

      if (approval.expires_at && new Date(approval.expires_at).getTime() < Date.now()) {
        return new Response(
          JSON.stringify({ error: "Approval has expired" }),
          { status: 403, headers: { ...corsHeaders, "Content-Type": "application/json" } },
        );
      }

      if (approval.repo_connection_id !== repo_connection_id) {
        return new Response(
          JSON.stringify({ error: "Approval scope mismatch: repo_connection_id" }),
          { status: 403, headers: { ...corsHeaders, "Content-Type": "application/json" } },
        );
      }

      if ((approval.branch_name ?? "") !== defaultBranch) {
        return new Response(
          JSON.stringify({ error: "Approval scope mismatch: branch_name" }),
          { status: 403, headers: { ...corsHeaders, "Content-Type": "application/json" } },
        );
      }

      // Derive effective scope paths from this request: union across patches,
      // or [] if any patch is unscoped or conductor_approved is set globally.
      const anyUnscoped = (body.conductor_approved ?? false)
        || patches.some(p => !p.scoped_paths || p.scoped_paths.length === 0);
      const effectiveScope = anyUnscoped
        ? []
        : Array.from(new Set(patches.flatMap(p => p.scoped_paths))).sort();

      if (!scopePathsEqual(approval.scope_paths ?? [], effectiveScope)) {
        return new Response(
          JSON.stringify({ error: "Approval scope mismatch: scope_paths" }),
          { status: 403, headers: { ...corsHeaders, "Content-Type": "application/json" } },
        );
      }
    }

    await supabase
      .from("execution_runs")
      .update({ status: "running" })
      .eq("id", execution_run_id);

    const { sha: baseSha, initialized: initializedDefaultBranch } = await ensureDefaultBranchSha(owner, repo, defaultBranch, ghToken);

    const globalApproval = body.conductor_approved ?? false;

    // Aggregated structured result for the whole run.
    const aggregate: ManifestExecutionResult & {
      branches: Array<{ agent?: string; branch: string; pr_url: string }>;
      prs: string[];
      blocked: Array<{ agent: string; reason: string }>;
      collisions: PathCollision[];
      backup_branch: string | null;
      handoffs_requested: Array<{ from_agent: string; to_lane: string; path: string; reason: "out_of_scope" }>;
    } = {
      written_files: [],
      deleted_files: [],
      skipped_files: [],
      out_of_scope: [],
      errors: [],
      branches: [],
      prs: [],
      blocked: [],
      collisions: [],
      backup_branch: null,
      handoffs_requested: [],
    };

    // Sprint A · B5 — automatic backup branch. Once per run, before any write,
    // for any existing repo. Skipped only when sessions.project_type='new'.
    // Never skippable by flag.
    let projectType: string | null = null;
    if (session_id) {
      const { data: ptRow } = await supabase
        .from("sessions")
        .select("project_type")
        .eq("id", session_id)
        .maybeSingle();
      projectType = (ptRow?.project_type as string | null) ?? null;
    }
    if (projectType !== "new" && !initializedDefaultBranch) {
      try {
        aggregate.backup_branch = await createBackupBranch(owner, repo, defaultBranch, ghToken);
        await supabase.from("audit_events").insert({
          user_id: userId,
          session_id: session_id ?? null,
          event_type: "backup_branch_created",
          actor: "github-execute",
          provider: "github",
          model: aggregate.backup_branch,
          execution_mode: runRow?.execution_mode ?? "",
          requires_approval: false,
          succeeded: true,
        });
      } catch (err) {
        const msg = err instanceof Error ? err.message : String(err);
        // Backup is never skippable. If we can't snapshot, refuse to write.
        await supabase
          .from("execution_runs")
          .update({ status: "failed", result: { error: "BACKUP_FAILED", message: msg } })
          .eq("id", execution_run_id);
        return new Response(
          JSON.stringify({
            error: "BACKUP_FAILED",
            message: `Could not create backup branch: ${msg}`,
          }),
          { status: 502, headers: { ...corsHeaders, "Content-Type": "application/json" } },
        );
      }
    }

    // Sprint A · B4 — collision detection. Compute once for the whole run.
    // Colliding paths are filtered out of each manifest before applyManifest.
    // Non-colliding files in the same patches still execute normally.
    const { collisions, collidingPaths } = detectCollisions(patches);
    aggregate.collisions = collisions;
    for (const c of collisions) {
      for (const agent of c.agents) {
        aggregate.skipped_files.push({
          path: c.path,
          reason: `collision: also claimed by ${c.agents.filter(a => a !== agent).join(", ")}`,
        });
      }
    }
    if (collisions.length > 0) {
      await supabase.from("audit_events").insert({
        user_id: userId,
        session_id: session_id ?? null,
        event_type: "collision_detected",
        actor: "github-execute",
        provider: "github",
        model: "",
        execution_mode: runRow?.execution_mode ?? "",
        requires_approval: false,
        succeeded: false,
      });
    }

    // Filter colliding entries out of each patch's manifest before execution.
    const filterManifest = (entries: FileManifestEntry[] | undefined) =>
      (entries ?? []).filter(e => !collidingPaths.has(e.path));

    if (mode === "per_agent") {
      for (const patch of patches) {
        const scopeCheck = checkAgentScope(patch, globalApproval);
        if (!scopeCheck.allowed) {
          aggregate.blocked.push({ agent: patch.agent_name, reason: scopeCheck.reason });
          continue;
        }

        // Loud failure: no fallback to maestro-patches/. If the agent didn't
        // emit a manifest, the run for this patch fails visibly.
        if (!patch.file_manifest || patch.file_manifest.length === 0) {
          aggregate.errors.push(
            `${patch.agent_name}: no file_manifest in response. Re-broadcast in Build mode.`,
          );
          aggregate.blocked.push({
            agent: patch.agent_name,
            reason: "no file_manifest emitted (agent must return real file changes, not prose)",
          });
          continue;
        }

        const filteredManifest = filterManifest(patch.file_manifest);
        if (filteredManifest.length === 0) {
          aggregate.blocked.push({
            agent: patch.agent_name,
            reason: "all manifest entries removed by collision detection",
          });
          continue;
        }

        const slug = patch.agent_name.toLowerCase().replace(/[^a-z0-9]+/g, "-");
        const branchName = `maestro/${slug}/run-${execution_run_id.slice(0, 8)}`;

        try {
          await createBranch(owner, repo, branchName, baseSha, ghToken);
        } catch (err) {
          const msg = err instanceof Error ? err.message : String(err);
          aggregate.errors.push(`${patch.agent_name}: branch create failed: ${msg}`);
          continue;
        }

        const baseMsg = patch.commit_message || `[Maestro] ${patch.agent_name} contribution`;
        const manifestResult = await applyManifest(
          filteredManifest,
          owner, repo, branchName,
          patch.scoped_paths ?? [],
          globalApproval || (patch.conductor_approved ?? false),
          baseMsg,
          ghToken,
        );

        aggregate.written_files.push(...manifestResult.written_files);
        aggregate.deleted_files.push(...manifestResult.deleted_files);
        aggregate.skipped_files.push(...manifestResult.skipped_files);
        aggregate.errors.push(...manifestResult.errors);

        // Sprint B · B4 — out-of-scope entries → handoff_requests
        if (manifestResult.out_of_scope.length > 0 && session_id) {
          const handoffRows = manifestResult.out_of_scope.map((e) => ({
            session_id,
            from_agent_id: patch.agent_id || null,
            from_agent_name: patch.agent_name,
            to_lane: e.path,
            request_type: "other" as const,
            description: `Agent ${patch.agent_name} attempted to write ${e.path} outside its declared scope`,
            payload: { path: e.path, content: e.content, operation: e.operation },
          }));
          await supabase.from("handoff_requests").insert(handoffRows);
          for (const e of manifestResult.out_of_scope) {
            aggregate.handoffs_requested.push({
              from_agent: patch.agent_name,
              to_lane: e.path,
              path: e.path,
              reason: "out_of_scope",
            });
          }
        }

        // Only open a PR if at least one file actually changed on this branch.
        const wrote = manifestResult.written_files.length + manifestResult.deleted_files.length;
        if (wrote === 0) {
          aggregate.blocked.push({
            agent: patch.agent_name,
            reason: "manifest produced zero writes (all entries skipped)",
          });
          continue;
        }

        const prBodyLines = [
          `## ${patch.agent_name}`,
          "",
          patch.content,
          "",
          "### Files changed",
          ...manifestResult.written_files.map(f => `- \`${f}\` (upsert)`),
          ...manifestResult.deleted_files.map(f => `- \`${f}\` (delete)`),
        ];
        if (manifestResult.skipped_files.length > 0) {
          prBodyLines.push("", "### Skipped");
          for (const s of manifestResult.skipped_files) {
            prBodyLines.push(`- \`${s.path}\` — ${s.reason}`);
          }
        }
        prBodyLines.push("", "---", "*Generated by Maestro orchestration*");

        const pr = await createPR(
          owner, repo, branchName, defaultBranch,
          `[Maestro] ${patch.agent_name}: ${patch.commit_message || "Agent contribution"}`,
          prBodyLines.join("\n"),
          ghToken,
        );

        aggregate.branches.push({ agent: patch.agent_name, branch: branchName, pr_url: pr.html_url });
        aggregate.prs.push(pr.html_url);
      }

    } else {
      // Synthesized mode: union of all patch manifests on a single branch.
      for (const patch of patches) {
        const scopeCheck = checkAgentScope(patch, globalApproval);
        if (!scopeCheck.allowed) {
          aggregate.blocked.push({ agent: patch.agent_name, reason: scopeCheck.reason });
        }
      }

      const allManifests = patches.flatMap(p => filterManifest(p.file_manifest).map(e => ({
        entry: e,
        scoped_paths: p.scoped_paths ?? [],
        conductor_approved: p.conductor_approved ?? false,
        agent_name: p.agent_name,
        agent_id: p.agent_id,
      })));

      if (allManifests.length === 0) {
        aggregate.errors.push("synthesized run: no file_manifest entries across any patch");
      } else if (aggregate.blocked.length === 0 || globalApproval) {
        const branchName = `maestro/synthesis/run-${execution_run_id.slice(0, 8)}`;
        try {
          await createBranch(owner, repo, branchName, baseSha, ghToken);

          // Apply each manifest entry under its originating patch's scope.
          // Last write wins on path collisions (synthesis-time conflict).
          const baseMsg = commit_message || "[Maestro] Synthesized patch";
          for (const { entry, scoped_paths, conductor_approved, agent_name, agent_id } of allManifests) {
            const single = await applyManifest(
              [entry], owner, repo, branchName, scoped_paths,
              globalApproval || conductor_approved, baseMsg, ghToken,
            );
            aggregate.written_files.push(...single.written_files);
            aggregate.deleted_files.push(...single.deleted_files);
            aggregate.skipped_files.push(...single.skipped_files);
            aggregate.errors.push(...single.errors);

            if (single.out_of_scope.length > 0 && session_id) {
              const handoffRows = single.out_of_scope.map((e) => ({
                session_id,
                from_agent_id: agent_id || null,
                from_agent_name: agent_name,
                to_lane: e.path,
                request_type: "other" as const,
                description: `Agent ${agent_name} attempted to write ${e.path} outside its declared scope`,
                payload: { path: e.path, content: e.content, operation: e.operation },
              }));
              await supabase.from("handoff_requests").insert(handoffRows);
              for (const e of single.out_of_scope) {
                aggregate.handoffs_requested.push({
                  from_agent: agent_name,
                  to_lane: e.path,
                  path: e.path,
                  reason: "out_of_scope",
                });
              }
            }
          }

          const wrote = aggregate.written_files.length + aggregate.deleted_files.length;
          if (wrote > 0) {
            const prBody = [
              "## Council Synthesis",
              "",
              synthesis_content || "",
              "",
              "### Files changed",
              ...aggregate.written_files.map(f => `- \`${f}\` (upsert)`),
              ...aggregate.deleted_files.map(f => `- \`${f}\` (delete)`),
              "",
              "---",
              "*Generated by Maestro orchestration*",
            ].join("\n");

            const pr = await createPR(
              owner, repo, branchName, defaultBranch,
              commit_message || "[Maestro] Synthesized council output",
              prBody, ghToken,
            );
            aggregate.branches.push({ branch: branchName, pr_url: pr.html_url });
            aggregate.prs.push(pr.html_url);
          }
        } catch (err) {
          const msg = err instanceof Error ? err.message : String(err);
          aggregate.errors.push(`synthesis branch failed: ${msg}`);
        }
      }
    }

    const totalWrote = aggregate.written_files.length + aggregate.deleted_files.length;
    const status: "success" | "partial" | "failed" =
      totalWrote === 0
        ? "failed"
        : (aggregate.skipped_files.length > 0 || aggregate.errors.length > 0 || aggregate.blocked.length > 0)
          ? "partial"
          : "success";

    const result: Record<string, unknown> = {
      status,
      written_files: aggregate.written_files,
      deleted_files: aggregate.deleted_files,
      skipped_files: aggregate.skipped_files,
      errors: aggregate.errors,
      branches: aggregate.branches,
      prs: aggregate.prs,
      blocked: aggregate.blocked,
      collisions: aggregate.collisions,
      backup_branch: aggregate.backup_branch,
      handoffs_requested: aggregate.handoffs_requested,
    };

    // Sprint B · B7 — auto-generate build report, advance phase, fire bouncer
    let buildReportId: string | null = null;
    if (session_id && totalWrote > 0 && runRow?.execution_mode !== "analyze") {
      const { data: reportRow } = await supabase
        .from("build_reports")
        .insert({
          session_id,
          files_written: aggregate.written_files,
          files_skipped: aggregate.skipped_files,
          collisions: aggregate.collisions,
          handoffs_pending: aggregate.handoffs_requested,
          pr_links: aggregate.prs,
          backup_branch: aggregate.backup_branch,
        })
        .select("id")
        .single();
      buildReportId = (reportRow?.id as string | undefined) ?? null;

      await supabase
        .from("sessions")
        .update({ current_phase: "bouncer" })
        .eq("id", session_id);

      // Fire-and-forget bouncer call. Frontend refetches bouncer_events
      // separately after build completes — do not block the response.
      const bouncerUrl = `${supabaseUrl}/functions/v1/bouncer`;
      fetch(bouncerUrl, {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          "Authorization": authHeader,
        },
        body: JSON.stringify({
          session_id,
          trigger: "end_of_build",
          files: aggregate.written_files,
        }),
      }).catch(() => { /* fire-and-forget */ });

      result.build_report_id = buildReportId;
    }

    await supabase
      .from("execution_runs")
      .update({
        status: status === "failed" ? "failed" : "complete",
        result,
        branch_name: aggregate.branches.map(b => b.branch).join(", "),
        pr_url: aggregate.branches.map(b => b.pr_url).join(", "),
      })
      .eq("id", execution_run_id);

    return new Response(JSON.stringify({ success: status !== "failed", result }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });

  } catch (err) {
    const message = err instanceof Error ? err.message : "Unknown error";

    try {
      const body = await req.clone().json().catch(() => ({})) as Record<string, string>;
      if (body.execution_run_id) {
        const supabaseUrl = Deno.env.get("SUPABASE_URL")!;
        const supabaseKey = Deno.env.get("SUPABASE_SERVICE_ROLE_KEY")!;
        const supabase = createClient(supabaseUrl, supabaseKey);
        await supabase
          .from("execution_runs")
          .update({ status: "failed", result: { error: message } })
          .eq("id", body.execution_run_id);
      }
    } catch { /* best effort */ }

    return new Response(JSON.stringify({ error: message }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});













