interface PathCollision {
  path: string;
  agents: string[];
}

// Sprint A · B4 — collision detection. If two agents claim the same file path,
// we block ONLY the colliding entries; non-colliding files in the same patches
// continue. Result includes the collisions array so the UI can surface them.
function detectCollisions(patches: AgentPatch[]): {
  collisions: PathCollision[];
  collidingPaths: Set<string>;
} {
  const pathMap = new Map<string, Set<string>>();
  for (const patch of patches) {
    if (!patch.file_manifest) continue;
    for (const entry of patch.file_manifest) {
      if (!entry?.path) continue;
      const set = pathMap.get(entry.path) ?? new Set<string>();
      set.add(patch.agent_name);
      pathMap.set(entry.path, set);
    }
  }
  const collisions: PathCollision[] = [];
  const collidingPaths = new Set<string>();
  for (const [path, agents] of pathMap) {
    if (agents.size > 1) {
      collisions.push({ path, agents: [...agents].sort() });
      collidingPaths.add(path);
    }
  }
  return { collisions, collidingPaths };
}

function scopePathsEqual(a: string[], b: string[]): boolean {
  if (a.length !== b.length) return false;
  const sa = [...a].sort();
  const sb = [...b].sort();
  return sa.every((v, i) => v === sb[i]);
}

/**
 * Check if a file path matches any of the scoped path globs.
 * Supports simple glob patterns: * matches within a segment, ** matches across segments.
 */
function pathMatchesScope(filePath: string, scopedPaths: string[]): boolean {
  if (scopedPaths.length === 0) return false;
  for (const pattern of scopedPaths) {
    if (globMatch(filePath, pattern)) return true;
  }
  return false;
}

function globMatch(path: string, pattern: string): boolean {
  // Normalize separators
  const p = path.replace(/\\/g, "/");
  const g = pattern.replace(/\\/g, "/");

  // Convert glob to regex
  let regex = "^";
  let i = 0;
  while (i < g.length) {
    if (g[i] === "*" && g[i + 1] === "*") {
      // ** matches any number of path segments
      regex += ".*";
      i += 2;
      if (g[i] === "/") i++; // skip trailing slash after **
    } else if (g[i] === "*") {
      // * matches anything except /
      regex += "[^/]*";
      i++;
    } else if (g[i] === "?") {
      regex += "[^/]";
      i++;
    } else if (".+^${}()|[]\\".includes(g[i])) {
      regex += "\\" + g[i];
      i++;
    } else {
      regex += g[i];
      i++;
    }
  }
  regex += "$";

  return new RegExp(regex).test(p);
}

interface ScopeCheckResult {
  allowed: boolean;
  reason: string;
}

function checkAgentScope(patch: AgentPatch, globalApproval: boolean): ScopeCheckResult {
  const hasPaths = patch.scoped_paths && patch.scoped_paths.length > 0;
  const approved = patch.conductor_approved || globalApproval;

  if (!hasPaths && !approved) {
    return {
      allowed: false,
      reason: `${patch.agent_name} has no scoped paths declared and no conductor approval. Write blocked.`,
    };
  }

  return { allowed: true, reason: "" };
}

function hasWriteManifest(patch: AgentPatch): boolean {
  return Array.isArray(patch.file_manifest) && patch.file_manifest.length > 0;
}

async function ghApi(path: string, token: string, options: RequestInit = {}) {
  const res = await fetch(`https://api.github.com${path}`, {
    ...options,
    headers: {
      Authorization: `Bearer ${token}`,
      "User-Agent": "Maestro",
      Accept: "application/vnd.github+json",
      "Content-Type": "application/json",
      ...((options.headers as Record<string, string>) || {}),
    },
  });
  const data = await res.json();
  if (!res.ok) throw new Error(data.message || `GitHub API ${res.status}`);
  return data;
}

async function createBranch(owner: string, repo: string, branchName: string, baseSha: string, token: string) {
  return ghApi(`/repos/${owner}/${repo}/git/refs`, token, {
    method: "POST",
    body: JSON.stringify({ ref: `refs/heads/${branchName}`, sha: baseSha }),
  });
}

function isEmptyRepoError(err: unknown): boolean {
  const message = err instanceof Error ? err.message : String(err);
  return /repository is empty/i.test(message);
}

async function ensureDefaultBranchSha(
  owner: string,
  repo: string,
  defaultBranch: string,
  token: string,
): Promise<{ sha: string; initialized: boolean }> {
  try {
    const ref = await ghApi(`/repos/${owner}/${repo}/git/ref/heads/${defaultBranch}`, token) as { object?: { sha?: string } };
    const sha = ref?.object?.sha;
    if (!sha) throw new Error(`Default branch ${defaultBranch} is missing a commit SHA.`);
    return { sha, initialized: false };
  } catch (err) {
    if (!isEmptyRepoError(err)) throw err;

    const tree = await ghApi(`/repos/${owner}/${repo}/git/trees`, token, {
      method: "POST",
      body: JSON.stringify({ tree: [] }),
    }) as { sha?: string };

    const rootCommit = await ghApi(`/repos/${owner}/${repo}/git/commits`, token, {
      method: "POST",
      body: JSON.stringify({
        message: "[Maestro] Initialize repository",
        tree: tree.sha,
        parents: [],
      }),
    }) as { sha?: string };

    if (!tree?.sha || !rootCommit?.sha) {
      throw new Error("Failed to initialize the default branch for an empty repository.");
    }

    try {
      await ghApi(`/repos/${owner}/${repo}/git/refs`, token, {
        method: "POST",
        body: JSON.stringify({ ref: `refs/heads/${defaultBranch}`, sha: rootCommit.sha }),
      });
    } catch (createErr) {
      const message = createErr instanceof Error ? createErr.message : String(createErr);
      if (!/reference already exists/i.test(message)) throw createErr;
    }

    const ref = await ghApi(`/repos/${owner}/${repo}/git/ref/heads/${defaultBranch}`, token) as { object?: { sha?: string } };
    const sha = ref?.object?.sha;
    if (!sha) throw new Error(`Default branch ${defaultBranch} could not be initialized.`);
    return { sha, initialized: true };
  }
}// Sprint A · B5 — automatic backup branch. Snapshots default branch HEAD
// before any write to an existing repo. Once per execution run, never
// skippable, no override flag. Returns the branch name.
async function createBackupBranch(
  owner: string,
  repo: string,
  defaultBranch: string,
  token: string,
): Promise<string> {
  const timestamp = new Date().toISOString().replace(/[:.]/g, "-").slice(0, 19);
  const backupBranch = `maestro-backup/${timestamp}`;
  const ref = await ghApi(`/repos/${owner}/${repo}/git/ref/heads/${defaultBranch}`, token);
  await ghApi(`/repos/${owner}/${repo}/git/refs`, token, {
    method: "POST",
    body: JSON.stringify({ ref: `refs/heads/${backupBranch}`, sha: ref.object.sha }),
  });
  return backupBranch;
}

async function getExistingFile(
  owner: string,
  repo: string,
  branch: string,
  path: string,
  token: string,
): Promise<{ sha: string; size: number } | null> {
  try {
    const data = await ghApi(
      `/repos/${owner}/${repo}/contents/${encodeURIComponent(path)}?ref=${branch}`,
      token,
    );
    return { sha: data.sha, size: data.size ?? 0 };
  } catch {
    return null;
  }
}

async function upsertFile(
  owner: string,
  repo: string,
  branch: string,
  path: string,
  content: string,
  message: string,
  token: string,
): Promise<void> {
  const existing = await getExistingFile(owner, repo, branch, path, token);

  // Length sanity guard: if the new file is dramatically smaller than the
  // existing one, the agent probably truncated. Skip with loud reason.
  // Only enforced when existing file is >200 bytes (avoids tripping on stubs).
  if (existing && existing.size > 200) {
    const newBytes = new TextEncoder().encode(content).length;
    if (newBytes < existing.size * 0.3) {
      throw new Error(
        `length sanity check failed: new content (${newBytes}b) is <30% of existing (${existing.size}b) for ${path}`,
      );
    }
  }

  const encoded = utf8ToBase64(content);
  const body: Record<string, unknown> = { message, content: encoded, branch };
  if (existing) body.sha = existing.sha;

  await ghApi(`/repos/${owner}/${repo}/contents/${encodeURIComponent(path)}`, token, {
    method: "PUT",
    body: JSON.stringify(body),
  });
}

async function deleteFile(
  owner: string,
  repo: string,
  branch: string,
  path: string,
  message: string,
  token: string,
): Promise<void> {
  const existing = await getExistingFile(owner, repo, branch, path, token);
  if (!existing) {
    // Already gone; treat as no-op rather than error.
    return;
  }
  await ghApi(`/repos/${owner}/${repo}/contents/${encodeURIComponent(path)}`, token, {
    method: "DELETE",
    body: JSON.stringify({ message, sha: existing.sha, branch }),
  });
}

async function applyManifest(
  manifest: FileManifestEntry[],
  owner: string,
  repo: string,
  branch: string,
  scopedPaths: string[],
  globalApproval: boolean,
  baseMessage: string,
  token: string,
): Promise<ManifestExecutionResult> {
  const out: ManifestExecutionResult = {
    written_files: [],
    deleted_files: [],
    skipped_files: [],
    out_of_scope: [],
    errors: [],
  };

  for (const entry of manifest) {
    const v = validateManifestEntry(entry);
    if (!v.ok) {
      out.skipped_files.push({ path: entry.path || "<unknown>", reason: v.reason });
      continue;
    }

    // Scope enforcement: every entry must match the patch's scoped_paths
    // unless conductor approval is set globally. Skip the entry, do NOT
    // abort the run — partial success beats total failure.
    const inScope = scopedPaths.length > 0 && pathMatchesScope(entry.path, scopedPaths);
    if (!inScope && !globalApproval) {
      // Sprint B · B4 — out-of-scope entries become handoff requests rather
      // than silent skips. Caller persists to handoff_requests.
      out.out_of_scope.push({
        path: entry.path,
        content: entry.content,
        operation: entry.operation,
      });
      out.skipped_files.push({
        path: entry.path,
        reason: "out of scope — handoff requested",
      });
      continue;
    }

    try {
      if (entry.operation === "delete") {
        await deleteFile(owner, repo, branch, entry.path, `${baseMessage} (delete ${entry.path})`, token);
        out.deleted_files.push(entry.path);
      } else {
        await upsertFile(
          owner, repo, branch, entry.path,
          entry.content as string,
          `${baseMessage} (${entry.path})`,
          token,
        );
        out.written_files.push(entry.path);
      }
    } catch (err) {
      const msg = err instanceof Error ? err.message : String(err);
      out.errors.push(`${entry.path}: ${msg}`);
      out.skipped_files.push({ path: entry.path, reason: `write failed: ${msg}` });
    }
  }

  return out;
}

